            PreparedStatement ps=CConexion.prepareStatement("INSERT INTO usuario(TipoDeUsuario,idUsuario,dni,NombreUsuario,ApellidoUsuario,ContraseÃ±a) VALUES(?,?,?,?,?,?)");
            ps.setString(1, combotipo.getSelectedItem().toString());
            ps.setString(2, txtid.getText());
            ps.setString(3, txtDNI.getText());
