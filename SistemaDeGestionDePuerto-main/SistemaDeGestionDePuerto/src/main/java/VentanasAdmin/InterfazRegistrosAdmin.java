    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("Inventario_id");
        modelo.addColumn("Producot_id");
        modelo.addColumn("Nombre");
        modelo.addColumn("Marca");
        modelo.addColumn("Cajas");
        modelo.addColumn("Unidades");        
        //modelo.addColumn("Descripcion");
        jtabledatos.setModel(modelo);
        int codigo_buscar;
        String consultasql="", nombre_buscar="", marca_buscar="";
        
        if(combotipo.getSelectedItem().toString()=="ID"){
            codigo_buscar = Integer.parseInt(txtTipo.getText());
            consultasql="SELECT i.inventario_id,i.producto_Producto_id, p.Nombre AS Nombre_Producto, p.Marca, i.Cantidad_Caja, i.Cantidad_Unidad FROM inventario i JOIN producto p ON i.producto_Producto_id = p.Producto_id WHERE  p.Producto_id = "+ codigo_buscar+";";
            
        }
        if (combotipo.getSelectedItem().toString()=="NOMBRE") {
            nombre_buscar = txtTipo.getText();
            consultasql = "SELECT i.inventario_id,i.producto_Producto_id, p.Nombre AS Nombre_Producto, p.Marca, i.Cantidad_Caja, i.Cantidad_Unidad FROM inventario i JOIN producto p ON i.producto_Producto_id = p.Producto_id WHERE  p.Nombre = '"+ nombre_buscar+"';"; 
        }
        if(combotipo.getSelectedItem().toString()=="MARCA"){
            marca_buscar = txtTipo.getText();
            consultasql="SELECT i.inventario_id,i.producto_Producto_id, p.Nombre AS Nombre_Producto, p.Marca, i.Cantidad_Caja, i.Cantidad_Unidad FROM inventario i JOIN producto p ON i.producto_Producto_id = p.Producto_id WHERE p.Marca = '"+ marca_buscar+"';";
        }
        String data[]=new String[6];
        Statement st;
        try{
            st=CConexion.createStatement();
            ResultSet rs=st.executeQuery(consultasql);
            while(rs.next()){
                data[0]=rs.getString(1);
                data[1]=rs.getString(2);
                data[2]=rs.getString(3);
                data[3]=rs.getString(4);
                data[4]=rs.getString(5);
                data[5]=rs.getString(6);
                //data[6]=rs.getString(7);
                modelo.addRow(data);
            }
        } catch(SQLException e){
            System.out.println("Error al mostrar Datos "+ e);
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

